import numpy as np
import pandas as pd
from datetime import datetime, timedelta


# ---------------------------------------------------------------------------
# Helper
# ---------------------------------------------------------------------------

def _validate_params(
    nominal_capacity_ah: float,
    nominal_voltage: float,
    max_voltage: float,
    min_voltage: float,
    eol_soh: float,
    years: int,
    rolling_window: int,
) -> None:
    """Raise ValueError for obviously wrong parameter combinations."""
    if max_voltage <= min_voltage:
        raise ValueError(f"max_voltage ({max_voltage}) must be > min_voltage ({min_voltage})")
    if not (0 < eol_soh < 100):
        raise ValueError(f"eol_soh must be in (0, 100), got {eol_soh}")
    if nominal_voltage < min_voltage or nominal_voltage > max_voltage:
        raise ValueError(
            f"nominal_voltage ({nominal_voltage}) must be between "
            f"min_voltage ({min_voltage}) and max_voltage ({max_voltage})"
        )
    if years <= 0:
        raise ValueError(f"years must be > 0, got {years}")
    if rolling_window < 2:
        raise ValueError(f"rolling_window must be >= 2, got {rolling_window}")
    if nominal_capacity_ah <= 0:
        raise ValueError(f"nominal_capacity_ah must be > 0, got {nominal_capacity_ah}")


# ---------------------------------------------------------------------------
# Main generator
# ---------------------------------------------------------------------------

def generate_advanced_lithium_dataset(
    filename: str = "advanced_lithium_5yr_dataset.csv",
    years: int = 5,
    freq: str = "1min",
    rolling_window: int = 60,
    # --- Battery parameters (easy to override for sensitivity studies) ---
    nominal_capacity_ah: float = 2.5,
    nominal_voltage: float = 3.7,
    max_voltage: float = 4.2,
    min_voltage: float = 3.0,
    eol_soh: float = 70.0,
    # --- Degradation knobs ---
    calendar_aging_rate: float = 10.0,   # % SoH lost over full lifespan (calendar)
    cycle_aging_factor: float = 0.02,    # % SoH lost per cycle
    temp_stress_threshold_c: float = 35.0,  # °C above which thermal stress begins
    temp_stress_factor: float = 0.05,    # stress coefficient per °C above threshold
    # --- Noise / randomness ---
    random_seed: int = 42,
    verbose: bool = True,
) -> pd.DataFrame:
    """
    Generate a synthetic lithium-ion battery telemetry dataset.

    Parameters
    ----------
    filename : str
        Output CSV path. Pass ``None`` to skip saving.
    years : int
        Simulation duration in years.
    freq : str
        Pandas-compatible time frequency string (e.g. ``"1min"``, ``"30s"``).
    rolling_window : int
        Window size (in samples) for rolling statistics.
    nominal_capacity_ah : float
        Fresh-cell rated capacity in Ah.
    nominal_voltage : float
        Nominal cell voltage (V).
    max_voltage : float
        Upper cut-off voltage (V).
    min_voltage : float
        Lower cut-off voltage (V).
    eol_soh : float
        End-of-life SoH threshold in percent (typically 70–80 %).
    calendar_aging_rate : float
        Total % capacity lost due to calendar aging over the full simulation.
    cycle_aging_factor : float
#         % capacity lost per full equivalent cycle.
    temp_stress_threshold_c : float
        Temperature (°C) above which thermal degradation begins.
    temp_stress_factor : float
        Degradation coefficient per °C above ``temp_stress_threshold_c``.
    random_seed : int
        NumPy random seed for reproducibility.
    verbose : bool
        Print progress messages.

    Returns
    -------
    pd.DataFrame
        The generated dataset (same data as the saved CSV).
    """
    _validate_params(
        nominal_capacity_ah, nominal_voltage, max_voltage, min_voltage, eol_soh, years, rolling_window
    )

    np.random.seed(random_seed)

    # ------------------------------------------------------------------
    # 1. Time index
    # ------------------------------------------------------------------
    start_time = datetime(2021, 1, 1)
    end_time = start_time + timedelta(days=365 * years)
    time_index = pd.date_range(start=start_time, end=end_time, freq=freq)
    n = len(time_index)

    if verbose:
        print(f"Generating {n:,} rows  ({years} yr @ {freq})…")

    # ------------------------------------------------------------------
    # 2. Environmental conditions
    # ------------------------------------------------------------------
    # Seasonal sinusoidal ambient temperature + Gaussian noise
    ambient_temp = 20 + 10 * np.sin(np.linspace(0, 20 * np.pi, n))
    ambient_temp += np.random.normal(0, 2, n)

    # ------------------------------------------------------------------
    # 3. Simulated load / current profile
    # ------------------------------------------------------------------
    # Mix of charge (negative) and discharge (positive) pulses
    load_profile = np.random.normal(0, 2.5, n)

    # ------------------------------------------------------------------
    # 4. State of Charge (SoC)
    # ------------------------------------------------------------------
    # Cumulative integration of load, clipped to [0, 100] %
    # FIX: use a bounded random walk so SoC doesn't drift unboundedly
    soc = np.empty(n)
    soc[0] = 50.0
    dt_h = 1 / 60  # 1-minute steps → hours
    for i in range(1, n):
        # Coulomb counting: ΔSoC = –I·Δt / C_nominal × 100
        delta = -load_profile[i] * dt_h / nominal_capacity_ah * 100
        soc[i] = np.clip(soc[i - 1] + delta, 0.0, 100.0)

    # ------------------------------------------------------------------
    # 5. Voltage model  (simplified Thevenin equivalent)
    # ------------------------------------------------------------------
    ocv = min_voltage + (soc / 100) * (max_voltage - min_voltage)
    # Internal resistance grows linearly with aging
    internal_resistance = 0.05 + np.linspace(0, 0.05, n)
    current = load_profile
    voltage = ocv - current * internal_resistance
    voltage += np.random.normal(0, 0.01, n)

    # ------------------------------------------------------------------
    # 6. Cell temperature  (thermal model)
    # ------------------------------------------------------------------
    # Joule heating proportional to I²·R; ambient provides baseline
    joule_heat = (current ** 2) * internal_resistance * 0.8   # simplified thermal gain
    temperature = ambient_temp + joule_heat
    temperature += np.random.normal(0, 1, n)

    # ------------------------------------------------------------------
    # 7. Cycle count  (full-equivalent cycle counting via Ah-throughput)
    # ------------------------------------------------------------------
    delta_soc = np.abs(np.diff(soc, prepend=soc[0]))
    cumulative_ah = np.cumsum(delta_soc / 100 * nominal_capacity_ah)
    cycle_count = cumulative_ah / nominal_capacity_ah

    # ------------------------------------------------------------------
    # 8. Degradation model  → SoH
    # ------------------------------------------------------------------
    calendar_aging = np.linspace(0, calendar_aging_rate, n)               # % over lifespan
    cycle_aging = cycle_count * cycle_aging_factor                         # % per cycle
    temp_stress = np.maximum(temperature - temp_stress_threshold_c, 0) * temp_stress_factor

    soh = 100 - (calendar_aging + cycle_aging + temp_stress)
    soh = np.clip(soh, eol_soh, 100)

    capacity_ah = nominal_capacity_ah * (soh / 100)

    # ------------------------------------------------------------------
    # 9. Remaining Useful Life (RUL)  – normalised to [0, 1]
    # ------------------------------------------------------------------
    # 1.0 = brand new, 0.0 = end-of-life
    rul = (soh - eol_soh) / (100 - eol_soh)
    rul = np.clip(rul, 0, 1)

    # ------------------------------------------------------------------
    # 10. Fault flags
    # ------------------------------------------------------------------
    over_voltage_flag = (voltage > max_voltage + 0.05).astype(int)
    under_voltage_flag = (voltage < min_voltage - 0.2).astype(int)
    over_temp_flag = (temperature > 60).astype(int)

    # ------------------------------------------------------------------
    # 11. Assemble base DataFrame
    # ------------------------------------------------------------------
    df = pd.DataFrame({
        "timestamp":                time_index,
        "ambient_temp_C":           ambient_temp,
        "battery_temp_C":           temperature,
        "current_A":                current,
        "voltage_V":                voltage,
        "soc_%":                    soc,
        "soh_%":                    soh,
        "capacity_Ah":              capacity_ah,
        "internal_resistance_ohm":  internal_resistance,
        "cycle_count":              cycle_count,
        "calendar_aging_%":         calendar_aging,
        "cycle_aging_%":            cycle_aging,
        "temperature_stress_%":     temp_stress,
        "RUL_normalized":           rul,
        "over_voltage_flag":        over_voltage_flag,
        "under_voltage_flag":       under_voltage_flag,
        "over_temperature_flag":    over_temp_flag,
    })

    # ------------------------------------------------------------------
    # 12. Rolling / EDA features
    # ------------------------------------------------------------------
    for col, alias in [
        ("voltage_V",       "voltage"),
        ("current_A",       "current"),
        ("battery_temp_C",  "temp"),
    ]:
        df[f"{alias}_roll_mean"] = df[col].rolling(rolling_window, min_periods=1).mean()
        df[f"{alias}_roll_std"]  = df[col].rolling(rolling_window, min_periods=1).std().fillna(0)

    # ------------------------------------------------------------------
    # 13. Gradient features  (anomaly detection)
    # ------------------------------------------------------------------
    df["dV_dt"]   = df["voltage_V"].diff().fillna(0)
    df["dT_dt"]   = df["battery_temp_C"].diff().fillna(0)
    df["dSoC_dt"] = df["soc_%"].diff().fillna(0)

    # ------------------------------------------------------------------
    # 14. Energy features
    # ------------------------------------------------------------------
    df["power_W"]               = df["voltage_V"] * df["current_A"]
    df["energy_Wh_cumulative"]  = (df["power_W"] / 60).cumsum()   # 1-min steps → Wh

    # ------------------------------------------------------------------
    # 15. Save
    # ------------------------------------------------------------------
    if filename is not None:
        df.to_csv(filename, index=False)
        if verbose:
            print(f"\n✓ Dataset saved → {filename}")

    if verbose:
        _print_summary(df, eol_soh)

    return df


# ---------------------------------------------------------------------------
# Summary printer
# ---------------------------------------------------------------------------

def _print_summary(df: pd.DataFrame, eol_soh: float) -> None:
    print("\n" + "=" * 55)
    print("  DATASET SUMMARY")
    print("=" * 55)
    print(f"  Rows               : {len(df):,}")
    print(f"  Columns            : {len(df.columns)}")
    print(f"  Time range         : {df['timestamp'].iloc[0]}  →  {df['timestamp'].iloc[-1]}")
    print(f"  Final SoH          : {df['soh_%'].iloc[-1]:.2f}%  (EoL = {eol_soh}%)")
    print(f"  Total cycles       : {df['cycle_count'].iloc[-1]:.2f}")
    print(f"  Final RUL          : {df['RUL_normalized'].iloc[-1]:.4f}")
    print(f"  Energy throughput  : {df['energy_Wh_cumulative'].iloc[-1]:.1f} Wh")
    print(f"  Fault counts       :")
    print(f"    Over-voltage     : {df['over_voltage_flag'].sum():,}")
    print(f"    Under-voltage    : {df['under_voltage_flag'].sum():,}")
    print(f"    Over-temperature : {df['over_temperature_flag'].sum():,}")
    print("=" * 55)


